def _ensure_webhook_tables(cursor):
    cursor.execute("""
        CREATE TABLE IF NOT EXISTS webhooks (
            id SERIAL PRIMARY KEY,
            entreprise_id INTEGER,
            url TEXT NOT NULL,
            events TEXT[] DEFAULT '{}',
            secret TEXT,
            active BOOLEAN DEFAULT TRUE,
            description TEXT,
            created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
            updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
        )
    """)
    # Defensive migration: older tenant schemas may pre-date several columns
    # that were added to the CREATE TABLE above. On ancient tenants the
    # `entreprise_id` column is NOT NULL (legacy schema), so we populate it
    # in the INSERT below rather than relying on a default.
    for col_name, col_type in (
        ("entreprise_id", "INTEGER"),
        ("events", "TEXT[] DEFAULT '{}'"),
        ("secret", "TEXT"),
        ("active", "BOOLEAN DEFAULT TRUE"),
        ("description", "TEXT"),
        ("created_at", "TIMESTAMP DEFAULT CURRENT_TIMESTAMP"),
        ("updated_at", "TIMESTAMP DEFAULT CURRENT_TIMESTAMP"),
    ):
        try:
            cursor.execute(f"ALTER TABLE webhooks ADD COLUMN IF NOT EXISTS {col_name} {col_type}")
        except Exception as exc:
            logger.warning("defensive ALTER webhooks.%s failed: %s", col_name, exc)
    cursor.execute("""
        CREATE TABLE IF NOT EXISTS webhook_deliveries (
            id SERIAL PRIMARY KEY,
            webhook_id INTEGER NOT NULL,
            event_type TEXT NOT NULL,
            payload JSONB,
            response_status INTEGER,
            response_body TEXT,
            success BOOLEAN DEFAULT FALSE,
            delivered_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
        )
    """)


