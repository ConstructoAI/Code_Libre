@router.get("/entreprise")
async def get_entreprise_config(user: ErpUser = Depends(get_current_user)):
    """Get all entreprise_config entries."""
    schema = user.schema
    if not schema:
        raise HTTPException(status_code=400, detail="Contexte entreprise requis")

    conn = db.get_conn()
    cursor = None
    try:
        db.set_tenant(conn, schema)
        cursor = conn.cursor()

        # Check if table exists
        cursor.execute(
            "SELECT EXISTS (SELECT FROM information_schema.tables "
            "WHERE table_schema = %s AND table_name = 'entreprise_config')",
            (schema,),
        )
        exists = cursor.fetchone()
        if not exists or not exists.get("exists", False):
            return {"items": []}

        cursor.execute(
            "SELECT id, config_data, created_at, updated_at "
            "FROM entreprise_config ORDER BY id"
        )
        items = []
        for row in cursor.fetchall():
            items.append({
                "id": row["id"],
                "config_data": row.get("config_data"),
                "created_at": str(row["created_at"]) if row.get("created_at") else None,
                "updated_at": str(row["updated_at"]) if row.get("updated_at") else None,
            })
        return {"items": items}
    except Exception as exc:
        logger.error("get_entreprise_config error: %s", exc)
        return {"items": []}
    finally:
        if cursor:
            cursor.close()
        try:
            db.reset_tenant(conn)
        except Exception:
            pass
        conn.close()


