# Tenant-wide color palette applied to every HTML document generated by
# the backend. Stored as a JSON value under the `document_theme` key of
# entreprise_config.config_data (same mechanism as logo_base64, company
# fields, etc.).

class DocumentThemeBody(BaseModel):
    """Partial theme body — missing keys fall back to defaults.

    Each value must be a hex color (#RGB or #RRGGBB). Unknown keys are
    silently dropped server-side by validate_document_theme.
    """
    primary: Optional[str] = None
    primary_dark: Optional[str] = None
    accent: Optional[str] = None
    accent_light: Optional[str] = None
    header_text: Optional[str] = None
    table_row_alt: Optional[str] = None
    info_bg: Optional[str] = None
    border: Optional[str] = None


@router.get("/document-theme")
async def get_document_theme_endpoint(user: ErpUser = Depends(get_current_user)):
    """Return the tenant's document color theme merged with defaults.

    Always succeeds — on any read error (missing table, corrupted JSON)
    the default Constructo palette is returned so the UI never breaks.
    """
    schema = user.schema
    if not schema:
        # Super-admin without tenant: return defaults
        return {"theme": dict(DEFAULT_DOCUMENT_THEME), "defaults": dict(DEFAULT_DOCUMENT_THEME)}

    conn = db.get_conn()
    cursor = None
    try:
        db.set_tenant(conn, schema)
        cursor = conn.cursor()
        from .html_utils import get_document_theme as _get_theme
        merged = _get_theme(cursor)
        return {"theme": merged, "defaults": dict(DEFAULT_DOCUMENT_THEME)}
    except Exception as exc:
        logger.error("get_document_theme error: %s", exc)
        return {"theme": dict(DEFAULT_DOCUMENT_THEME), "defaults": dict(DEFAULT_DOCUMENT_THEME)}
    finally:
        if cursor:
            cursor.close()
        try:
            db.reset_tenant(conn)
        except Exception:
            pass
        conn.close()


@router.put("/document-theme")
async def update_document_theme_endpoint(
    body: DocumentThemeBody,
    user: ErpUser = Depends(require_role("admin")),
):
    """Update the tenant's document color theme (admin only).

    Merges the request body into the current theme. To reset a single
    color to its default, the client should send the default value
    explicitly (frontend exposes a "Réinitialiser" button). An entirely
    empty body is a no-op.
    """
    schema = user.schema
    if not schema:
        raise HTTPException(status_code=400, detail="Contexte entreprise requis")

    # Build dict from non-None fields and validate hex format
    provided = {k: v for k, v in body.model_dump().items() if v is not None}
    if not provided:
        return {"message": "Aucune modification", "theme": dict(DEFAULT_DOCUMENT_THEME)}
    try:
        validated = validate_document_theme(provided)
    except ValueError as exc:
        raise HTTPException(status_code=400, detail=str(exc))

    # Only persist the keys the caller actually sent (normalized), not the
    # full merged palette — otherwise a partial update would overwrite any
    # defaults the user hadn't touched.
    to_store = {k: validated[k] for k in provided.keys() if k in validated}

    conn = db.get_conn()
    cursor = None
    # The pool hands out connections in AUTOCOMMIT — without toggling
    # autocommit off, `SELECT FOR UPDATE` releases its row lock as soon as
    # the SELECT statement commits, making the subsequent UPDATE racy
    # (two concurrent PUTs would overwrite each other's colors).
    prev_autocommit = conn.autocommit
    try:
        conn.autocommit = False
        db.set_tenant(conn, schema)
        cursor = conn.cursor()

        # Read current config (handles both text and jsonb column types).
        # With autocommit=False, FOR UPDATE holds the row lock until commit,
        # serializing concurrent PUTs against the same row.
        cursor.execute("SELECT config_data FROM entreprise_config WHERE id = 1 FOR UPDATE")
        row = cursor.fetchone()
        if row:
            raw = row.get("config_data")
            if isinstance(raw, dict):
                current = raw
            elif isinstance(raw, str) and raw.strip():
                try:
                    current = json.loads(raw)
                except (ValueError, TypeError):
                    current = {}
            else:
                current = {}
            existing_theme = current.get("document_theme") or {}
            if isinstance(existing_theme, str):
                try:
                    existing_theme = json.loads(existing_theme)
                except (ValueError, TypeError):
                    existing_theme = {}
            if not isinstance(existing_theme, dict):
                existing_theme = {}
            existing_theme.update(to_store)
            current["document_theme"] = existing_theme
            cursor.execute(
                "UPDATE entreprise_config SET config_data = %s, "
                "updated_at = CURRENT_TIMESTAMP WHERE id = 1",
                (json.dumps(current),),
            )
        else:
            current = {"document_theme": to_store}
            cursor.execute(
                "INSERT INTO entreprise_config (config_data, created_at, updated_at) "
                "VALUES (%s, CURRENT_TIMESTAMP, CURRENT_TIMESTAMP)",
                (json.dumps(current),),
            )
        conn.commit()

        # Return the fully merged theme so the UI can refresh
        merged = dict(DEFAULT_DOCUMENT_THEME)
        merged.update(current.get("document_theme") or {})
        return {"message": "Thème de document mis à jour", "theme": merged}
    except HTTPException:
        try:
            conn.rollback()
        except Exception:
            pass
        raise
    except Exception as exc:
        logger.error("update_document_theme error: %s", exc)
        try:
            conn.rollback()
        except Exception:
            pass
        raise HTTPException(status_code=500, detail="Erreur lors de la mise à jour du thème")
    finally:
        if cursor:
            cursor.close()
        try:
            conn.autocommit = prev_autocommit
        except Exception:
            pass
        try:
            db.reset_tenant(conn)
        except Exception:
            pass
        conn.close()


