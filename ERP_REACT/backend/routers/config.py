@router.post("/webhooks/{webhook_id}/test")
async def test_webhook(webhook_id: int, user: ErpUser = Depends(require_role("admin"))):
    """Send a test ping to a webhook (admin only)."""
    schema = user.schema
    if not schema:
        raise HTTPException(status_code=400, detail="Contexte entreprise requis")

    conn = db.get_conn()
    cursor = None
    try:
        db.set_tenant(conn, schema)
        cursor = conn.cursor()
        _ensure_webhook_tables(cursor)

        cursor.execute("SELECT id, url FROM webhooks WHERE id = %s", (webhook_id,))
        row = cursor.fetchone()
        if not row:
            raise HTTPException(status_code=404, detail="Webhook non trouvé")

        webhook_url = row["url"]
        payload = {
            "event": "test.ping",
            "timestamp": datetime.now().isoformat(),
            "test": True,
        }

        success = False
        status_code = None
        response_body = None
        message = ""

        try:
            resp = _requests.post(webhook_url, json=payload, timeout=10)
            status_code = resp.status_code
            response_body = resp.text[:2000] if resp.text else ""
            success = 200 <= resp.status_code < 300
            message = "Test envoye avec succes" if success else f"Le serveur a repondu avec le code {status_code}"
        except _requests.exceptions.ConnectionError:
            message = "Impossible de se connecter au serveur"
        except _requests.exceptions.Timeout:
            message = "Le serveur n'a pas repondu dans le delai imparti"
        except _requests.exceptions.RequestException as req_exc:
            message = f"Erreur lors de l'envoi: {str(req_exc)}"

        # Record delivery
        cursor.execute(
            "INSERT INTO webhook_deliveries (webhook_id, event_type, payload, response_status, response_body, success) "
            "VALUES (%s, %s, %s::jsonb, %s, %s, %s)",
            (webhook_id, "test.ping", json.dumps(payload), status_code, response_body, success),
        )
        conn.commit()

        return {"success": success, "status_code": status_code, "message": message}
    except HTTPException:
        raise
    except Exception as exc:
        logger.error("test_webhook error: %s", exc)
        conn.rollback()
        raise HTTPException(status_code=500, detail="Erreur lors du test du webhook")
    finally:
        if cursor:
            cursor.close()
        try:
            db.reset_tenant(conn)
        except Exception:
            pass
        conn.close()


