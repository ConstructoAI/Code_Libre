@router.post("/users")
async def create_user(
    body: UserCreate,
    user: ErpUser = Depends(require_role("admin")),
):
    """Create a new user (admin only)."""
    schema = user.schema
    if not schema:
        raise HTTPException(status_code=400, detail="Contexte entreprise requis")

    if len(body.password) < 6:
        raise HTTPException(status_code=400, detail="Le mot de passe doit avoir au moins 6 caracteres")

    password_hash = hash_password(body.password)

    conn = db.get_conn()
    cursor = None
    try:
        db.set_tenant(conn, schema)
        cursor = conn.cursor()

        # Check for duplicate username
        cursor.execute("SELECT id FROM users WHERE username = %s", (body.username,))
        if cursor.fetchone():
            raise HTTPException(status_code=409, detail="Ce nom d'utilisateur existe deja")

        cursor.execute(
            "INSERT INTO users (username, password_hash, email, full_name, role, is_admin, active, created_at) "
            "VALUES (%s, %s, %s, %s, %s, %s, TRUE, CURRENT_TIMESTAMP) RETURNING id",
            (body.username, password_hash, body.email, body.full_name,
             body.role, body.is_admin),
        )
        row = cursor.fetchone()
        conn.commit()
        return {"id": row["id"] if row else 0, "message": "Utilisateur créé avec succès"}
    except HTTPException:
        raise
    except Exception as exc:
        logger.error("create_user error: %s", exc)
        conn.rollback()
        raise HTTPException(status_code=500, detail="Erreur lors de la création de l'utilisateur")
    finally:
        if cursor:
            cursor.close()
        try:
            db.reset_tenant(conn)
        except Exception:
            pass
        conn.close()


@router.put("/users/{user_id}")
async def update_user(
    user_id: int,
    body: UserUpdate,
    user: ErpUser = Depends(require_role("admin")),
):
    """Update a user (admin only)."""
    schema = user.schema
    if not schema:
        raise HTTPException(status_code=400, detail="Contexte entreprise requis")

    conn = db.get_conn()
    cursor = None
    try:
        db.set_tenant(conn, schema)
        cursor = conn.cursor()

        # Build dynamic update
        updates = []
        params = []
        if body.email is not None:
            updates.append("email = %s")
            params.append(body.email)
        if body.full_name is not None:
            updates.append("full_name = %s")
            params.append(body.full_name)
        if body.role is not None:
            updates.append("role = %s")
            params.append(body.role)
        if body.is_admin is not None:
            updates.append("is_admin = %s")
            params.append(body.is_admin)
        if body.preferences_json is not None:
            updates.append("preferences_json = %s::jsonb")
            params.append(json.dumps(body.preferences_json))

        if not updates:
            return {"message": "Aucune modification"}

        params.append(user_id)
        query = f"UPDATE users SET {', '.join(updates)} WHERE id = %s RETURNING id"
        cursor.execute(query, tuple(params))
        row = cursor.fetchone()
        if not row:
            raise HTTPException(status_code=404, detail="Utilisateur non trouvé")
        conn.commit()
        return {"message": "Utilisateur mis à jour"}
    except HTTPException:
        raise
    except Exception as exc:
        logger.error("update_user error: %s", exc)
        conn.rollback()
        raise HTTPException(status_code=500, detail="Erreur lors de la mise à jour")
    finally:
        if cursor:
            cursor.close()
        try:
            db.reset_tenant(conn)
        except Exception:
            pass
        conn.close()


@router.put("/users/{user_id}/password")
async def change_user_password(
    user_id: int,
    body: PasswordChange,
    user: ErpUser = Depends(get_current_user),
):
    """Change a user's password (admin or self)."""
    schema = user.schema
